using System;

namespace CharacterSequence
{
    class CharacterSequence
    {
        static void Main(string[] args)
        {
            string input = Console.ReadLine();

            if (input == "softuni")
            {
                for (int i = 0; i < input.Length; i++)
                {
                    Console.WriteLine(input[i]);
                }
            }
            else
            {
                for (int i = 0; i < input.Length; i++)
                {
                    Console.WriteLine(input[i]);
                }
            }
        }
    }
}
